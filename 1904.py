N = int(input())

# N = 1 -> 1
# N = 2 -> 00, 11
# N = 3 -> 100, 001, 111
# N = 4 -> 0011, 0000, 1001, 1100, 1111
# N = 5 -> 11000, 10001, 00011, 11100, 00111, 11001, 10011, 11111
# N = [N - 1] + [N - 2]

A = 0
i1 = 1
i2 = 2

